// 검색 함수: 유저 메시지에서 카테고리 찾기
export function findCategories(message: string): string[] {
  const found: string[] = [];
  const sortedKeys = Object.keys(categoryDict).sort((a, b) => b.length - a.length);
  for (const key of sortedKeys) {
    if (message.includes(key)) {
      found.push(...categoryDict[key]);
      break;
    }
  }
  return found.length > 0 ? found : [];
}
